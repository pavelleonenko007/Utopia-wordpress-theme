						<div class="art-rich">
							<p class="descr-art">The concert in his Athens. The double program that he is about to bring to Salzburg. Then two dates in Italy in November. All with the new Utopia Orchestra. “La Lettura” meets the Greek conductor: “The sense of drama matters, it’s the secret, nature, life.”</p>
							<div class="rich-right-side w-richtext">
								<blockquote>“The sense of drama matters, it’s the secret, nature, life.”</blockquote>
								<p>He’s like the Meltemi, the north wind that blows over the Aegean Sea. Elusive and tireless. Greek conductor Teodor Currentzis (1972) has finished his long and highly acclaimed European tour with the new Utopia Orchestra in his hometown, beloved Athens. Sold out months before. Five thousand tickets disappeared for the performance of Gustav Mahler’s Third Symphony at the Odeon of Herodes Atticus, an (open-air) theater on the southern slope of the Acropolis. An enchanted place, built between 161 and 174 AD, with steps arranged in 32 rows, which on the night of the concert appeared suspended under a threatening stormy sky.<br><br><br><br>The meeting with the maestro is scheduled for the next day at the hotel. It’s raining, Currentzis arrives with his assistant and Ilya Shakhov, CEO and General Manager of the Utopia Orchestra. Black and white shoes, rebellious tuft of hair, dark tight-fitting shirt and pants, the Greek conductor appears serene and, as he sits down, he sinks back into the large cushions of a couch. His voice is deep, firm. It has a hypnotic rhythm.</p>
								<p><em>Maestro, we are in your city and you conducted in a magical place…</em></p>
								<p>«When I was 4 years old, my mother took me to the Herodes Atticus for the first concert of my life. Since then, I have always dreamed of conducting here, one day. It happened (raises both arms, editor’s note), and I imagined it just as it was: a homecoming, with a sky slightly rainy and partly sunny, friends, family, music, the podium, my mother sitting in the front row…»</p>
								<p><em>How did you feel walking through the orchestra to then ascend to the podium?</em>‍</p>
								<p>«It was the last concert of the tour, after Berlin, Vienna… I told myself, „Come on, you conduct at home, it will be relaxing“.</p>
								<figure class="w-richtext-align-center w-richtext-figure-type-image">
									<div><img src="<?php echo get_template_directory_uri(); ?>/images/65eed45ef431c7f947f5ee21_18201.png" loading="lazy" alt></div>
									<figcaption>Teodor Currentzis, 2023</figcaption>
								</figure>
								<p><em>And instead?<br><br></em>«And instead, I felt tense, full of responsibility. I wanted to shower the world with love… It was as if I said to the child inside me: „Your moment has come, now it’s your turn“. It may seem surreal, but it’s as if the true Teodor Currentzis was among the audience watching the young Teodor Currentzis conducting».</p>
								<p><em>The choice of Mahler’s “Third” Symphony?<br><br></em>«In the 1980s, it was the symphony I loved the most, the one that seduced my soul. I wanted to become a composer, and Mahler was my hero. As a young boy, I listened to his music with my first love; she and I always listened to the Third Symphony».</p>
								<p><em>Let’s talk about the new orchestra, Utopia, which made its debut in October 2022.<br><br></em>«In every orchestra, you can find people lacking enthusiasm, but also special individuals, full of eagerness to do things, and those are the ones who make a difference. With Utopia, I wanted to create an orchestra with a special energy, formed only by people of that kind, who come from the best orchestras in the world and bring their enthusiasm with them».</p>
								<figure class="w-richtext-align-center w-richtext-figure-type-image">
									<div><img src="<?php echo get_template_directory_uri(); ?>/images/65eeeb5cce10ce25c51bff67_4.png" loading="lazy" alt></div>
									<figcaption>Santa Cecilia Hall</figcaption>
								</figure>
								<p><em>There are many different nationalities…<br><br></em>«One of the purposes is also to bring together different cultures. Just in the viola section, for example, we have 14 nationalities. In total, the musicians, who are about 160, come from 31 countries. Let me add, and it’s crucial, that these musicians, besides being highly prepared like those in many other orchestras, also have spiritual motivations. Within the ranks of Utopia, there is an incredible atmosphere of a youth orchestra: they love and respect each other. It is a new vision of democracy in an orchestra».</p>
								<figure style="max-width:1280pxpx" class="w-richtext-align-fullwidth w-richtext-figure-type-image">
									<div><img src="<?php echo get_template_directory_uri(); ?>/images/65eeeba859b27db2c8d82634_Utopians.png" loading="lazy" alt></div>
								</figure>
								<p><em>What is the most beautiful sensation you feel when conducting them?<br><br>‍</em>«The sensation of being able to reach truly far, farther than what the crescendos and diminuendos suggest… I’m talking about things that go beyond skill and technical preparation. Here, there is also love, which represents 50% of the whole matter. I want to understand who the musicians really are, their identity, why they are doing this, what their deepest motivations are. And how willing they are to set aside their ego to connect with others».</p>
