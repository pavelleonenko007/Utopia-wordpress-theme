										<h2>The Conductor</h2>
										<p>Teodor Currentzis (Athens, February 24, 1972; pictured during rehearsals with the Utopia Orchestra, photo by Alfonso Salgueiro; in the photo on the other page by Michalis Kloukinas, in Athens where he was a guest at the Athens Epidaurus Festival) began his musical studies at a very young age and in 1994 went to further his studies at the St. Petersburg Conservatory with Ilya Musin. From 2004 to 2011, he was the director of the Opera in Novosibirsk, Siberia, where he founded the musicAeterna Orchestra and Choir, and from 2011 to 2019, he served as the Artistic Director of the Perm Opera, also in Russia. In 2018, he was appointed Principal Conductor of the Stuttgart Radio Symphony Orchestra in Germany. His name has gained prominence in the music scene since 2016. In the same year, the magazine “Opernwelt” named him “best conductor.” He has performed on the podium at prestigious festivals and theaters, collaborating with directors such as Robert Wilson, Peter Sellars, Theodoros Terzopoulos, Romeo Castellucci, and Dmitri Tcherniakov. Currentzis records for the Sony Classical record label (teodor-currentzis.com).</p>
