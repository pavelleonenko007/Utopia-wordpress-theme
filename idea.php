								<p>The idea of Utopia emerged several years ago. Its point of departure is the simple truth that the willingness of musicians to devote themselves to serious preparatory and research work is just as important as musical mastery in order to achieve visionary artistic goals. Therefore, Utopia is not so much an orchestra in the conventional sense of the word, but rather a special creative community, a team of like-minded people with a shared musical ideology. They join together to create without compromise what their musical imagination comes up with in order to find the best sound.<br><br><br>The musicians come together specifically to create each new programme. The cast of the orchestra varies depending on the instruments stipulated in the score. Musicians from dozens of countries take part in the project, such as Armenia, Australia, Austria, Belgium, Belarus, Bulgaria, Canada, Chile, China, Finland, France, Germany, Greece, Hungary, Israel, Italy, Japan, Kazakhstan, the Netherlands, Portugal, Romania, Russia, Serbia, Spain, Switzerland, Turkey, the UK, Ukraine, the USA, Venezuela, and many others.</p>
