								<p>Teodor Currentzis is the Founder and Artistic Director of the Utopia Orchestra, the Chief Conductor of the SWR Symphony Orchestra Stuttgart, and the Artistic Director of musicAeterna Orchestra and Choir.<br>‍<br><br>Teodor was born in Greece, where he began studying music. In 1994, he entered St. Petersburg State Conservatory to study under the legendary professor Ilya Musin.<br>‍<br><br>Together with his ensembles, Teodor Currentzis regularly tours Europe and the world with performances in numerous prestigious venues including Vienna Konzerthaus, Berlin Philharmonic, Elbphilharmonie Hamburg, Munich Philharmonic, Philharmonie de Paris, Kölner Philharmonie, Auditorio Nacional, Baden-Baden Festspielhaus, and La Scala. As a stage conductor and musical director, Teodor Currentzis has worked with the leading opera theatres including Opéra de Paris, Bayerische Staatsoper, Opernhaus Zürich, Teatro Real, and the Bolshoi Theatre.</p>
