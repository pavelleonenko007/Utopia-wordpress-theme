								<p>Three tours of the new orchestra have been scheduled for the 2022/23 season. The first Utopia concerts were given in October 2022. The orchestra performed Igor Stravinsky's and Maurice Ravel's pieces in some of Europe’s most important venues supporting the project, namely Philharmonie Luxembourg, Laeiszhalle Hamburg, Wiener Konzerthaus, and also in the Berliner Philharmonie. In June 2023, in a series of concerts Utopia presented its interpretation of Gustav Mahler's Symphony No. 3. In November 2023, it will present the programme with Johannes Brahms’ Violin Concerto and Pyotr Tchaikovsky’s Symphony No. 5. This summer, Utopia will make its debut at the Salzburg Festival with concert performance of Purcell’s The Indian Queen and Mozart’s c-Moll Messe.</p>
